package main

import "fmt"

func main() {
	fmt.Println("Eko")
	fmt.Println("Eko Kurniawan")
	fmt.Println("Eko Kurniawan Khannedy")

	fmt.Println(len("Eko"))                  // len = mengambil jumlah karakter
	fmt.Println("Eko Kurniawan"[0])          // mengambil index ke 0 (1 nya)
	fmt.Println("Eko Kurniawan Khannedy"[1]) // mengambil index ke 1 (2 nya)
}
